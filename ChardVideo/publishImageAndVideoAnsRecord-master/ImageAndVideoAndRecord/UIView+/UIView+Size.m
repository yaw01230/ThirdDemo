}

- (void)setTabBarPushToViewLeftItemNil{
    
    [self getParentviewController].navigationController.navigationBar.tintColor = [UIColor whiteColor];
    [self getParentviewController].tabBarController.navigationItem.backBarButtonItem = [[UIBarButtonItem alloc]initWithTitle:@"" style:UIBarButtonItemStylePlain target:nil action:nil];
}


@end
